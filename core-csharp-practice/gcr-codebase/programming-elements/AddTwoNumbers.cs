using System;
class AddTwoNumbers{
	public static void Main(string[] args){
		//Ask user to input first number
		int a=int.Parse(Console.ReadLine());
		//Ask user to input second number
		int b=int.Parse(Console.ReadLine());
		//add two numbers and result store in x
		int x=a+b;
		//print x
		Console.WriteLine(x);
	}
}