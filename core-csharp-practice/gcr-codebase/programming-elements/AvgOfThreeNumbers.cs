using System;
class AvgOfThreeNumbers{
	public static void Main(string[] args){
		//take first number as input
		int a=3;
		//take second number as input
		int b=12;
		//take third number as input
		int c=6;
		//calculate average
		int avg=(a+b+c)/3;
		//print average 
		Console.WriteLine(avg);
	}
}